// Generate a random 7-day meal plan
export function generateMealPlan() {
  const shuffleAndPick = (arr, count) => {
    const shuffled = [...arr].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
  };

  const breakfasts = shuffleAndPick(BREAKFAST_MEALS, 7);
  const lunches = shuffleAndPick(LUNCH_MEALS, 7);
  const dinners = shuffleAndPick(DINNER_MEALS, 7);

  const days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'];
  const today = new Date();
  const dayOfWeek = today.getDay();
  // Reorder so today is first
  const startIdx = dayOfWeek === 0 ? 6 : dayOfWeek - 1;

  return days.map((_, index) => {
    const dayIdx = (startIdx + index) % 7;
    const breakfast = breakfasts[index];
    const lunch = lunches[index];
    const dinner = dinners[index];
    const totalKj = breakfast.kj + lunch.kj + dinner.kj;

    const date = new Date(today);
    date.setDate(today.getDate() + index);

    return {
      day: days[dayIdx],
      date: date.toISOString().split('T')[0],
      isToday: index === 0,
      breakfast,
      lunch,
      dinner,
      totalKj,
    };
  });
}
